package H;
// Ternary operator
public class H {
    public static void main(String[] args)
    {
        int x = 0;
        boolean isEven = false;
        System.out.println ("x =" + x);
        x = isEven? 4: 7; // vale? True: false --> ternary operator
        System.out.println ("x =" + x);
    }
}
